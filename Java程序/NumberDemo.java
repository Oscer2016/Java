import java.util.*;

public class NumberDemo {
    public static void main(String[] args) {
        //十进制转二进制
        System.out.println(Integer.toBinaryString(19));
        //十进制转十六进制
        System.out.println(Integer.toHexString(19));
        //十进制转八进制
        System.out.println(Integer.toOctalString(19));
    }
}

