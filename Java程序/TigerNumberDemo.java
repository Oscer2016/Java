import java.util.*;

public class TigerNumberDemo {
    public static void main(String[] args) {
        //%n是指输出平台特定的换行字符，如果是windows会置换为r\n,如果使linux则会置换为\n
        System.out.printf("%d%n",19);
        System.out.printf("%o%n",19);
        System.out.printf("%x%n",19);
    }
}

